# Testa as desigualdades.
menor = 1 < 2
print(menor)
print(1 < 2)

maior_ou_igual = 1 >= 2
print(maior_ou_igual)
print(1 >= 2)

# Isso sim é um igual e não uma atribuição.
igual = 1 == 2
print(igual)
print(1 == 2)

# Testa se é diferente.
diferente = 1 != 2
print(diferente)
print(1 != 2)

# Testa se contém.
contem_i = 'i' in 'Insper'
print(contem_i)
print('i' in 'Insper')

contem_I = 'I' in 'Insper'
print(contem_I)
print('I' in 'Insper')